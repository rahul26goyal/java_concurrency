package com.rahulg.netty.sample1;

public class ResponseData {

    private int intValue;

    public ResponseData() {}
    public ResponseData(int val) {
        intValue = val;
    }

    public void setIntValue(int intValue) {
        this.intValue = intValue;
    }

    public int getIntValue() {
        return intValue;
    }
}
